                    controlMedicamentos.eliminarMedicamento(codigo);
                    valida.desplegarMensajeDialogo(this, "se ha realizado la operacion satisfactoriamente ","Proceso Exitoso" , JOptionPane.INFORMATION_MESSAGE);
