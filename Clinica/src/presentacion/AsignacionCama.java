    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean control = true;
        String cedula = cedula_paciente.getText();
        String cod_cama = codigo_cama.getText();
        String fecha_realizacion = fecha.getText();
        control = control && valida.capturarEntero(rootPane, cedula, "Cedula");
        control = control && valida.capturarEntero(rootPane, cod_cama, "Codigo Cama");
        control = control && valida.ValidaVacios(rootPane, fecha_realizacion, "Fecha Realizacion");
        if (control)
        {
            if ("Libre".equals(control_cama.consultarCama(cod_cama).getEstado())) 
            {
                if (control_cama.asignarCama(cedula, cod_cama, fecha_realizacion) > 0)
                {
                    valida.desplegarMensajeDialogo(rootPane, "La asignacion de la cama se ha realizado satisfactoriamente ", "Proceso Exitoso", JOptionPane.INFORMATION_MESSAGE);
                }
                else 
                {
                    valida.desplegarMensajeDialogo(rootPane, "La cama ya ha sido asignada ", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
            else 
            {
                valida.desplegarMensajeDialogo(rootPane, "La cama Esta Ocupada ", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

