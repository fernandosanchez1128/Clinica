    private void btEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarActionPerformed
        String codigo = codigo_liberar.getText();
        if (valida.capturarEntero(rootPane, codigo, "codigo"))
        {
            Cama cama = control_cama.consultarCama(codigo);
            cama.setEstado("Libre");
            if (control_cama.editarCamaCod(cama.getCodCama(), cama) > 0)
            {
                valida.desplegarMensajeDialogo(rootPane, "la cama ha sido actualizada", "", WIDTH);
            }
            else 
            {
                valida.desplegarMensajeDialogo(rootPane, "no hay cama con ese codigo", "ERROR", WIDTH);
            }
        }
    }//GEN-LAST:event_btEliminarActionPerformed

